        public static string GetTriangleType(int sideA, int sideB, int sideC)
        {
            if (sideA == sideB && sideB == sideC)
            {
                return "Equilateral";
            }
            else if (sideA == sideB || sideB == sideC || sideA == sideC)
            {
                return "Isoscales";
            }
            else
            {
                return "Scalene";
            }
        }


