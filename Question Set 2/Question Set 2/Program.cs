        public static string GetSquareColor(int row, char column)
        {
            bool isRowEven = row % 2 == 0;
            bool IsColumnEven = ((int)column - (int)'a') % 2 == 0;
            if (isRowEven)
            {
                return IsColumnEven ? "Black" : "White";
            }
            else
            {
                return isRowEven ? "White" : "Black";
