        public static int LargestNumber(int a, int b, int c)
        {
            if (a >= b && a >= c)
                return a;
            else if (b >= a && b >= c)
                return b;
            else
                return c;
        }
        // NO 10
        public static int SmallestNumber(int a, int b, int c)
        {
            if (a <= b && a <= c)
                return a;
            else if (b <= a && b <= c)
                return b;
            else
                return c;
        }

        // No 10
        public static int LargestFourNumber(int a, int b, int c, int d)
        {
            if (a >= b && a >= c && a >= d)
                return a;
            else if (b >= a && b >= c && b >= d)
                return b;
            else if (c >= a && c >= b && c >= d)
                return c;
            else
                return d;
        }

